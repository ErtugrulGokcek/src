package Gun15;

public class _02_ForLoop {
    public static void main(String[] args) {
        // 1 den 5 e kadar olan sayilari ekrana yazdiriniz

        int i=1;
        while (i<=5) {
            System.out.println("i = " + i);
            i++;
        } // 12345
        // for dongusu

        for (i=1;i<=5;i++) {
            System.out.println("i = " + i);
    }


    }



}

