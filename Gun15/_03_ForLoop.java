package Gun15;

public class _03_ForLoop {
    public static void main(String[] args) {
        // 0 dan 10 dahil kadar sayilari ekrana yazdiriniz
        // 10 dan 0 dahil kadar sayilari ekrana yazdiriniz

        int i=0;
        int j=10;

        for (i=0;i<=10;i++) {
            System.out.println("i = " + i);
        }

        for (j=10;j>=0;j--) {
            System.out.println("j = " + j);
        }


    }
}
